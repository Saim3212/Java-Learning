package JavaBasics;

public class Switch {
    public static void main(String[] args) {
        int num = 10;
        switch (num){
            case 1 : 
            System.out.println("Num1");
            break;
            case 2 : 
            System.out.println("Num 2");
            break;
            case 3 : 
            System.out.println("Num3");
            break;
            default : 
            System.out.println("NOTA");
        }
    }
}
